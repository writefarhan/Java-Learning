public class Person {

    String Name;
    int Phone;
    String Email;

    public void setName(String n){


        Name = n;

    }

    public void setPhone(int p){


        Phone = p;


    }

    public void setEmail(String e) {


        Email = e;

    }


    public String getName(){


        return Name;

    }

    public int getPhone(){


        return Phone;

    }

    public String getEmail(){


        return Email;

    }

}
